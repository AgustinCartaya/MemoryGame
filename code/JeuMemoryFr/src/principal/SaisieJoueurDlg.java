    /**
     * Action du bouton "Parcourrir" qui ouvre un JFileChooser
     * pour obtenir l'URL de l'image du joueur
     * @param evt source de l'événement
     */
    private void btParcourrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btParcourrirActionPerformed
        JFileChooser jf= new JFileChooser("./src/img");
        if (jf.showOpenDialog(this)== JFileChooser.APPROVE_OPTION)
        {
            String path = jf.getSelectedFile().getPath();
            Image img=Toolkit.getDefaultToolkit().getImage(path);
            img=img.getScaledInstance(btPhoto.getWidth()-10, btPhoto.getHeight()-10, Image.SCALE_DEFAULT);
            ImageIcon photo1 = new ImageIcon(img);
            btPhoto.setIcon(photo1);
        } 
    }//GEN-LAST:event_btParcourrirActionPerformed

