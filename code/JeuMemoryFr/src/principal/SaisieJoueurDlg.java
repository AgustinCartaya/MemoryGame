    /**
     * verifie que tous les champs nécessaires sont remplis
     * correptement et si c'est le cas, ajoute le joueur a la liste de joueurs,
     * sinon une erreur est affiché en forme de fenêtre contextuelle
     * après la fenêtre est fermé
     * @param evt source de l'événement
     * @see LesJoueurs#ajouteJoueur(principal.Joueur) 
     */
    private void btValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderActionPerformed
        if(this.tfPseudo.getText() != null && !this.tfPseudo.getText().equals("")
            && this.ltFamilles.getSelectedIndex() != -1
            && this.btPhoto.getIcon()!= null ){
            
                this.newJoueur = new Joueur(this.tfPseudo.getText(),
                this.dlm.getElementAt( this.ltFamilles.getSelectedIndex()).toString());
                this.newJoueur.setPhoto( (ImageIcon)this.btPhoto.getIcon() );
                this.newJoueur.initPaquetTest();
                this.lj.ajouteJoueur(this.newJoueur);
                this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "SVP saisir les informations nécessaires ",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
            
    }//GEN-LAST:event_btValiderActionPerformed



